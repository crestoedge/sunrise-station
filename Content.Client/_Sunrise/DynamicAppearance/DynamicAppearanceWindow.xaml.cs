namespace Content.Client._Sunrise.DynamicAppearance;

/// <summary>
/// In-round appearance editor window (BUI).
/// Split across partial classes:
///   - <c>DynamicAppearanceWindow.xaml.cs</c>  — fields, ctor, public API
///   - <c>DynamicAppearanceWindow.Handlers.cs</c> — control event wiring
///   - <c>DynamicAppearanceWindow.State.cs</c>    — state push / refresh helpers
///   - <c>DynamicAppearanceWindow.Preview.cs</c>  — character preview management
/// </summary>
[GenerateTypedNameReferences]
public sealed partial class DynamicAppearanceWindow : DefaultWindow
{
    // ═══════════ Public events ═══════════

    /// <summary>Fired when the user presses Save.</summary>
    public event Action? OnSave;

    /// <summary>Fired when the user presses Reset.</summary>
    public event Action? OnReset;

    // ═══════════ Dependencies ═══════════

    private readonly IEntityManager _entManager;
    private readonly IPrototypeManager _protoMan;
    private readonly MarkingManager _markingManager;
    private readonly IConfigurationManager _cfg;
    private readonly HumanoidAppearanceSystem _humanoidSystem;
    private readonly InventorySystem _inventorySystem;

    // ═══════════ Draft state ═══════════

    private DynamicAppearanceState _draftState = new(
        new MarkingSet(),
        string.Empty,
        Sex.Male,
        0,
        Gender.Male,
        string.Empty,
        Color.White,
        Color.White,
        new Dictionary<HumanoidVisualLayers, CustomBaseLayerInfo>(),
        1f,
        1f
    );

    public DynamicAppearanceState DraftState
    {
        get => _draftState;
        private set => _draftState = value;
    }

    // ═══════════ Controls created programmatically ═══════════

    private ColorSelectorSliders _skinColorSelector = default!;

    /// <summary>Tone slider for species that use unary (toned) skin coloration, e.g. humans.</summary>
    private Slider _skinToneSlider = default!;

    // ═══════════ OptionButton backing lists ═══════════

    private readonly List<Sex> _sexValues = new();
    private readonly List<Gender> _genderValues = new();
    private List<TTSVoicePrototype> _filteredVoices = new();
    private readonly bool _ttsEnabled;

    // ═══════════ Cached data ═══════════

    private SpeciesPrototype? _speciesProto;

    // ═══════════ Preview ═══════════

    /// <summary>EntityUid resolved from the server-provided <see cref="NetEntity"/>.</summary>
    private EntityUid _previewEntity = EntityUid.Invalid;

    /// <summary>Client-side dummy for clothes-off preview.</summary>
    private EntityUid _dummyEntity = EntityUid.Invalid;

    private bool _showClothes = true;
    private Direction _previewRotation = Direction.North;

    // ═══════════ Constructor ═══════════

    public DynamicAppearanceWindow()
    {
        RobustXamlLoader.Load(this);

        _entManager = IoCManager.Resolve<IEntityManager>();
        _protoMan = IoCManager.Resolve<IPrototypeManager>();
        _markingManager = IoCManager.Resolve<MarkingManager>();
        _cfg = IoCManager.Resolve<IConfigurationManager>();
        _humanoidSystem = _entManager.System<HumanoidAppearanceSystem>();
        _inventorySystem = _entManager.System<InventorySystem>();

        _ttsEnabled = _cfg.GetCVar(SunriseCCVars.TTSEnabled);
        TTSContainer.Visible = _ttsEnabled;

        // Wire all control events (see Handlers partial)
        InitSaveResetHandlers();
        InitAgeHandlers();
        InitSexHandlers();
        InitPronounsHandlers();
        InitVoiceHandlers();
        InitSizeHandlers();
        InitColorHandlers();
        InitMarkingHandlers();
        InitPreviewHandlers();
    }

    protected override void ExitedTree()
    {
        base.ExitedTree();
        DestroyDummy();
    }
}
